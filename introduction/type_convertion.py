# Convertion of types
# Function type() show the type of variable

value = 10;
print(type(value));

value = float(value);
print(type(value));

# Division always return float, but if put // return int
print(10 / 2);  #float
print(10 // 2); #int

# Number and string
value = '10';
print(type(value));

value = int(value);
print(type(value));

#'f' means a call the variables 
print(f'value = {value}');


